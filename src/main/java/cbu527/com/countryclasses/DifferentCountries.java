package cbu527.com.countryclasses;

public class DifferentCountries {
    public int USA,GreatBritian,Canada,Unknown;

    public DifferentCountries(){
        USA =0;
        GreatBritian =0;
        Canada =0;
        Unknown =0;
    }

    public int getUSA() {
        return USA;
    }

    public void setUSA(int USA) {
        this.USA = USA;
    }

    public int getGreatBritian() {
        return GreatBritian;
    }

    public void setGreatBritian(int greatBritian) {
        this.GreatBritian = greatBritian;
    }

    public int getCanada() {
        return Canada;
    }

    public void setCanada(int canada) {
        this.Canada = canada;
    }

    public int getUnknown() {
        return Unknown;
    }

    public void setUnknown(int unknown) {
        this.Unknown = unknown;
    }
}
